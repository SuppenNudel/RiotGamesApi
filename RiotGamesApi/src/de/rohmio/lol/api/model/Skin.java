package de.rohmio.lol.api.model;

/**
 * This object contains champion skin data.
 * @author Cedric
 *
 */
public class Skin {

	private Integer num;
	private String name;
	private Integer id;

	public Integer getNum() {
		return num;
	}
	public String getName() {
		return name;
	}
	public Integer getId() {
		return id;
	}

}
