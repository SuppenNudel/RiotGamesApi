package de.rohmio.lol.api.model;

/**
 * This object contains image data.
 * @author Cedric
 *
 */
public class Image {

	private String full;
	private String group;
	private String sprite;
	private Integer h;
	private Integer w;
	private Integer y;
	private Integer x;

	public String getFull() {
		return full;
	}
	public String getGroup() {
		return group;
	}
	public String getSprite() {
		return sprite;
	}
	public Integer getH() {
		return h;
	}
	public Integer getW() {
		return w;
	}
	public Integer getY() {
		return y;
	}
	public Integer getX() {
		return x;
	}

}
